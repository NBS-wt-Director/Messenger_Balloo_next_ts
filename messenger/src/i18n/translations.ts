// Fallback языки - копируют русский для отсутствующих переводов
const createFallback = (base: Translations): Translations => ({ ...base });

// Полный объект translationsData с fallback языками
export const translationsData: Record<Language, Translations> = {
  ...baseTranslationsData,
  be: createFallback(baseTranslationsData.ru),
  ba: createFallback(baseTranslationsData.ru),
  cv: createFallback(baseTranslationsData.ru),
  sah: createFallback(baseTranslationsData.ru),
  udm: createFallback(baseTranslationsData.ru),
  ce: createFallback(baseTranslationsData.ru),
  os: createFallback(baseTranslationsData.ru),
} as unknown as Record<Language, Translations>;

export function t(key: keyof Translations, lang: Language = 'ru'): string {
  return translationsData[lang]?.[key] || translationsData.ru[key] || key;
}

// Хелпер для получения всех переводов для языка
export function getTranslations(lang: Language): Translations {
  return translationsData[lang] || translationsData.ru;
}

// Обратная совместимость - позволяет использовать translations.xxx
export function translations(lang: Language): Translations {
  return getTranslations(lang);
}
